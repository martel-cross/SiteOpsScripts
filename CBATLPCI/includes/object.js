function XCreateObject(name)
{
	var obj = null;
	if( name.substr(0, 18) == "MSXML2.DOMDocument" )
	{
		var MIN_VERSION = 3;
		var MAX_VERSION = 5;

		for( v=MIN_VERSION; v<=MAX_VERSION && (obj == null); v++ )
		{
			try
			{
				obj = new ActiveXObject("MSXML2.DOMDocument."+v+".0");
			}
			catch (e) { /* no action */ }
		}
	}
	else
	{
		obj = new ActiveXObject(name);
	}

	return obj;
}

